    {
        echo "<h1>Categoria</h1>";
        echo "<p>#{$category->id} category title: {$category->title}. </p>";

        $posts = $category->posts()->get();

        if($posts){
            echo "<h2>Post</h2>";
            foreach($posts as $post){
              echo "<p>#{$post->id} post title: {$post->title}. </p>";
            }
        }
    }

